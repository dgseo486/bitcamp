// 변수 - 메모리에 값 저장하기
package step02;

class Exam03_3 {
    public static void main(String[] args){
        int a;
        a=100;

        int b;
        b=a;

        System.out.println(a);
        System.out.println(b);
    }
}

// 변수에 값 할당(assignment)
// - 메모리에 값을 저장하는 것을 말한다.
// 문법
// - 변수명 = 변수 또는 리터럴;
// 용어
// - '=' 왼쪽에 있는 변수를 'l-value'라 부른다.
// - '=' 오른쪽에 있는 변수나 리터럴을 'r-value'라 부른다.